package string;

public class endsWith {

	public static void main(String[] args) {
		String c="teeju";
		String d=new String("Teeja");
		
		System.out.println(c.endsWith("u"));
		System.out.println(c.endsWith("eju"));
		System.out.println(d.endsWith("ja"));
		System.out.println(d.endsWith("u"));

	}

}
