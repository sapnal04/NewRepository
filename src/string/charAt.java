package string;

public class charAt {

	public static void main(String[] args) {
		String i="Welcome to India.";
		
		System.out.println(i.charAt(0));
		System.out.println(i.charAt(7));//white space at 7th index
		System.out.println(i.charAt(11));
		System.out.println(i.charAt(16));
		//System.out.println(i.charAt(22));//java.lang.StringIndexOutOfBoundsException

	}

}
