package loops;

public class For {

	public static void main(String[] args) {
	
		
	int a;
	for(a=3;a<=33;a=a+3)
	{System.out.println(a);
	System.out.println("Tannu");}
	
	int i;
	for(i=1;i<=10;i++)
	{System.out.println("2 * "+i+" ="+2*i);}
	
	System.out.println("========");
	
	for(int b=40;b>=0;b--)
	{if(b!=4)
		{System.out.println();}}
	
	System.out.println("======");
	
	for(int g=0;g<=10;g++)
	{System.out.println("4 * "+g+" = "+(4*g));}

	}
}
