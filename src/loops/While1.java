package loops;

public class While1 {

	public static void main(String[] args) {
		int i=1000;
		while(i>=100)
		{if(i%100==0)
		{System.out.println(i);}
		i--;}
		
		
		System.out.println("=======");
		
		
		int j=0;
		while(j<=40)
		{
			if(j%5==0)
			{System.out.println(j);}
			j++;}
		
		System.out.println("=====");
		
		int f = 0;
		while(f<=5)
		{System.out.println(f);
		f++;}
		

	}

}
